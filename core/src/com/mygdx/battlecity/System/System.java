package com.mygdx.battlecity.System;

import com.mygdx.battlecity.Tickable;

public abstract class System extends Tickable {

    @Override
    public void OnActivate() {
        super.OnActivate();
    }

    @Override
    public void OnDeactivate() {
        super.OnDeactivate();
    }

    @Override
    public void OnTick(float dt) {
        super.OnTick(dt);
    }
}
